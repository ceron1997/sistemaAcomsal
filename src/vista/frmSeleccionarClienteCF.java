    private void txtBuscarNombreKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtBuscarNombreKeyPressed
        // TODO add your handling code here:
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            limpiar_tableClienteSCF();
            listarClienteSimilares();

            if (tableClienteSCF != null && tableClienteSCF.getRowCount() > 0) {
                //btnSeleccionarProd.requestFocus();
                tableClienteSCF.requestFocus();
                tableClienteSCF.changeSelection(0, 0, false, false);// selecciona la primera liena 
            }
        }

    }//GEN-LAST:event_txtBuscarNombreKeyPressed

