    public void listarClienteSimilares() {
        String nombre = txtBuscarNombre.getText();
        List<ClienteCF> listarCl = objClienteCFDAO.buscarNombresSimilares(nombre); // conexio BD
        modelo = (DefaultTableModel) tableClienteSCF.getModel();
        Object[] ob = new Object[5];
        for (int i = 0; i < listarCl.size(); i++) {

            ob[0] = listarCl.get(i).getDui();
            ob[1] = listarCl.get(i).getNombre();
            ob[2] = listarCl.get(i).getNombreNegocio();
            ob[3] = listarCl.get(i).getDireccion();
            ob[4] = listarCl.get(i).getNit();
            modelo.addRow(ob);
        }

        tableClienteSCF.setModel(modelo);
    }

    public void limpiar_tableClienteSCF() {
        for (int i = 0; i < modelo.getRowCount(); i++) {
            modelo.removeRow(i);
            i--;
        }
    }

    public void SeleccionarRegistro() {

        nombre = String.valueOf(modelo.getValueAt(tableClienteSCF.getSelectedRow(), 1));
    }

